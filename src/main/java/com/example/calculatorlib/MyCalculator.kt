package com.example.calculatorlib

object MyCalculator {

    val add: (Double, Double) -> Double = { a,b -> a+b }

    val subtract: (Double, Double) -> Double = { a,b -> a-b }

    val multiply: (Double, Double) -> Double = { a,b -> a*b }

    val divide: (Double, Double) -> Double = { a,b -> a/b }

}